package srp_solid;

public class MemberClass {

	private String name;
	private String memberId;
	
	public MemberClass(String name, String memberId) {
		this.name = name;
		this.memberId = memberId;
	}
	
	public String getName() {
		return name;
	}
	
	public String getMemberId() {
		return memberId;
	}
	
}
